#Homework 8.1: The mood checker

mood = input("Please enter your mood: ")

if mood == "happy":
    print("It is great to see you happy!")

elif mood == "nervous":
    print("Take a deep breath 3 times.")

elif mood == "sad":
    print("Cheer up! Go google some kitten pictures ASAP!")

elif mood == "excited":
    print("I'm glad you're excited!")

elif mood == "relaxed":
    print("Stay calm and carry on my dude :)")

else:
    print("I don't recognize this mood")